a=5;
b=2;

console.log("a + b = ",a+b);//addition
console.log("a - b = ",a-b);//subtration
console.log("a * b = ",a*b);//multiplication
console.log("a / b = ",a/b);//division
console.log("a % b = ",a%b);//give remainder (modulus)
console.log("a ** b = ",a**b);//exponentiation exp.5^2

// Unary operators
console.log("a++ = ",a++," b++ = ",b++);//increment a++=a+1 or ++a both valid
console.log("a-- = ",a--," b-- = ",b--);//decrement b--=b-1
